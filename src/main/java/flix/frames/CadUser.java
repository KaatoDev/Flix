    private boolean select() {
        JRadioButton[] aa = new JRadioButton[5];
        aa[0] = acaoR;
        aa[1] = comediaR;
        aa[2] = romanceR;
        aa[3] = suspenseR;
        aa[4] = terrorR;
        int i=0;
        for (JRadioButton a : aa) {
            if (a.isSelected())
                i++;
            if (i==2)
                return false;
        }
        return true;
    }
